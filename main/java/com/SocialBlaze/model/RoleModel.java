package com.SocialBlaze.model;

public class RoleModel {
    private int roleId;
    private String name;

    public RoleModel() {
    }

    public RoleModel(int roleId, String name) {
        this.roleId = roleId;
        this.name = name;
    }

    public int getRoleId() {
        return roleId;
    }

    public void setRoleId(int roleId) {
        this.roleId = roleId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
